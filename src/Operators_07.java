public class Operators_07 {
    public static void main(String[] args) {
        int a = 4;

        int b = 6 % a;  //modulo operator

        System.out.println(b);
        System.out.println(6==6);  // comparison operator
        System.out.println(64>69);
        System.out.println(64> 5 && 64<3);    //logic operators   and
        System.out.println(64>5 || 64<3);      //    or

    }
}



