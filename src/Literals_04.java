//LITERALS :- literals are the values that we give to variables thats it


public class Literals_04 {

    // So today we will talk about java Literals
    public static void main(String[] args) {
        int age = 34;
        byte age1= 40;
        System.out.println(age);
// by default for decimals the compiler uses double
        // but we can use f to tell its float  (this os only for double and float)
        float num1 = 3.4f;
        double num2 = 4.5;
        System.out.println(num1);

        // for long also we have to put l at last just like double and float
        long num3 = 7575748574845L;

        boolean a = true;
        System.out.println(a);

        String name = "ARJUN";
        System.out.println(name);

    }
}
